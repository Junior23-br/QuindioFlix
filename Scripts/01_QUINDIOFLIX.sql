   4. EMPLEADOS Y DEPARTAMENTOS
   ============================================================ */

CREATE TABLE DEPARTAMENTOS (
    ID_DEPARTAMENTO      NUMBER(10) GENERATED BY DEFAULT ON NULL AS IDENTITY,
    NOMBRE_DEPARTAMENTO  VARCHAR2(80) NOT NULL,
    DESCRIPCION          VARCHAR2(300),
    ID_EMPLEADO_JEFE     NUMBER(10),
    ESTADO               VARCHAR2(20) DEFAULT 'ACTIVO' NOT NULL,

    CONSTRAINT PK_DEPARTAMENTOS PRIMARY KEY (ID_DEPARTAMENTO),
    CONSTRAINT UQ_DEP_NOMBRE UNIQUE (NOMBRE_DEPARTAMENTO),
    CONSTRAINT CK_DEP_ESTADO CHECK (ESTADO IN ('ACTIVO', 'INACTIVO'))
);

COMMENT ON TABLE DEPARTAMENTOS IS 'Departamentos internos de QuindioFlix.';
COMMENT ON COLUMN DEPARTAMENTOS.ID_DEPARTAMENTO IS 'Identificador unico del departamento.';
COMMENT ON COLUMN DEPARTAMENTOS.NOMBRE_DEPARTAMENTO IS 'Nombre del departamento.';
COMMENT ON COLUMN DEPARTAMENTOS.DESCRIPCION IS 'Descripcion del departamento.';
COMMENT ON COLUMN DEPARTAMENTOS.ID_EMPLEADO_JEFE IS 'Empleado jefe del departamento.';
COMMENT ON COLUMN DEPARTAMENTOS.ESTADO IS 'Estado del departamento.';




CREATE TABLE EMPLEADOS (
    ID_EMPLEADO      NUMBER(10) GENERATED BY DEFAULT ON NULL AS IDENTITY,
    ID_DEPARTAMENTO  NUMBER(10) NOT NULL,
    ID_SUPERVISOR    NUMBER(10),
    NOMBRE           VARCHAR2(120) NOT NULL,
    EMAIL            VARCHAR2(150) NOT NULL,
    TELEFONO         VARCHAR2(30),
    CARGO            VARCHAR2(100) NOT NULL,
    FECHA_INGRESO    DATE NOT NULL,
    ESTADO           VARCHAR2(20) DEFAULT 'ACTIVO' NOT NULL,

    CONSTRAINT PK_EMPLEADOS PRIMARY KEY (ID_EMPLEADO),
    CONSTRAINT UQ_EMPLEADOS_EMAIL UNIQUE (EMAIL),
    CONSTRAINT CK_EMP_ESTADO CHECK (ESTADO IN ('ACTIVO', 'INACTIVO')),
    CONSTRAINT CK_EMP_SUP_DIF CHECK (
        ID_SUPERVISOR IS NULL OR ID_SUPERVISOR <> ID_EMPLEADO
    ),
    CONSTRAINT FK_EMP_DEP FOREIGN KEY (ID_DEPARTAMENTO)
        REFERENCES DEPARTAMENTOS(ID_DEPARTAMENTO),
    CONSTRAINT FK_EMP_SUP FOREIGN KEY (ID_SUPERVISOR)
        REFERENCES EMPLEADOS(ID_EMPLEADO) ON DELETE SET NULL
);

COMMENT ON TABLE EMPLEADOS IS 'Empleados de QuindioFlix organizados por departamento y jerarquia interna.';
COMMENT ON COLUMN EMPLEADOS.ID_EMPLEADO IS 'Identificador unico del empleado.';
COMMENT ON COLUMN EMPLEADOS.ID_DEPARTAMENTO IS 'Departamento al que pertenece el empleado.';
