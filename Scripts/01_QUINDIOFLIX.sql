SET DEFINE OFF;

ALTER SESSION SET NLS_DATE_FORMAT = 'YYYY-MM-DD';

/* ============================================================
   1. TABLAS BASE: CIUDADES, PLANES Y USUARIOS
   ============================================================ */

CREATE TABLE CIUDADES (
    ID_CIUDAD        NUMBER(10) GENERATED BY DEFAULT ON NULL AS IDENTITY,
    NOMBRE_CIUDAD   VARCHAR2(80) NOT NULL,
    DEPARTAMENTO    VARCHAR2(80) NOT NULL,
    PAIS            VARCHAR2(60) DEFAULT 'Colombia' NOT NULL,
    ESTADO          VARCHAR2(20) DEFAULT 'ACTIVO' NOT NULL,

    CONSTRAINT PK_CIUDADES PRIMARY KEY (ID_CIUDAD),
    CONSTRAINT UQ_CIUDADES_NOM_DEP UNIQUE (NOMBRE_CIUDAD, DEPARTAMENTO),
    CONSTRAINT CK_CIUDADES_ESTADO CHECK (ESTADO IN ('ACTIVO', 'INACTIVO'))
);

COMMENT ON TABLE CIUDADES IS 'Ciudades donde residen los usuarios de QuindioFlix.';
COMMENT ON COLUMN CIUDADES.ID_CIUDAD IS 'Identificador unico de la ciudad.';
COMMENT ON COLUMN CIUDADES.NOMBRE_CIUDAD IS 'Nombre de la ciudad.';
COMMENT ON COLUMN CIUDADES.DEPARTAMENTO IS 'Departamento al que pertenece la ciudad.';
COMMENT ON COLUMN CIUDADES.PAIS IS 'Pais de residencia. Para este proyecto se usa Colombia.';
COMMENT ON COLUMN CIUDADES.ESTADO IS 'Estado de la ciudad dentro del sistema: ACTIVO o INACTIVO.';


CREATE TABLE PLANES_SUSCRIPCION (
    ID_PLAN                 NUMBER(10) GENERATED BY DEFAULT ON NULL AS IDENTITY,
    NOMBRE_PLAN             VARCHAR2(40) NOT NULL,
    PANTALLAS_SIMULTANEAS   NUMBER(2) NOT NULL,
    MAX_PERFILES            NUMBER(2) NOT NULL,
    CALIDAD_VIDEO           VARCHAR2(10) NOT NULL,
    PRECIO_MENSUAL          NUMBER(10,2) NOT NULL,
    ESTADO                  VARCHAR2(20) DEFAULT 'ACTIVO' NOT NULL,

    CONSTRAINT PK_PLANES_SUSCRIPCION PRIMARY KEY (ID_PLAN),
    CONSTRAINT UQ_PLANES_NOMBRE UNIQUE (NOMBRE_PLAN),
    CONSTRAINT CK_PLANES_NOMBRE CHECK (NOMBRE_PLAN IN ('BASICO', 'ESTANDAR', 'PREMIUM')),
    CONSTRAINT CK_PLANES_PANTALLAS CHECK (PANTALLAS_SIMULTANEAS > 0),
    CONSTRAINT CK_PLANES_MAX_PERFILES CHECK (MAX_PERFILES > 0),
    CONSTRAINT CK_PLANES_CALIDAD CHECK (CALIDAD_VIDEO IN ('SD', 'HD', '4K')),
    CONSTRAINT CK_PLANES_PRECIO CHECK (PRECIO_MENSUAL > 0),
    CONSTRAINT CK_PLANES_ESTADO CHECK (ESTADO IN ('ACTIVO', 'INACTIVO'))
);

COMMENT ON TABLE PLANES_SUSCRIPCION IS 'Planes de suscripcion disponibles en QuindioFlix.';
COMMENT ON COLUMN PLANES_SUSCRIPCION.ID_PLAN IS 'Identificador unico del plan.';
COMMENT ON COLUMN PLANES_SUSCRIPCION.NOMBRE_PLAN IS 'Nombre del plan: BASICO, ESTANDAR o PREMIUM.';
COMMENT ON COLUMN PLANES_SUSCRIPCION.PANTALLAS_SIMULTANEAS IS 'Cantidad maxima de pantallas simultaneas permitidas por el plan.';
COMMENT ON COLUMN PLANES_SUSCRIPCION.MAX_PERFILES IS 'Cantidad maxima de perfiles permitidos por cuenta segun el plan.';
COMMENT ON COLUMN PLANES_SUSCRIPCION.CALIDAD_VIDEO IS 'Calidad maxima permitida por el plan: SD, HD o 4K.';
COMMENT ON COLUMN PLANES_SUSCRIPCION.PRECIO_MENSUAL IS 'Valor mensual del plan.';
COMMENT ON COLUMN PLANES_SUSCRIPCION.ESTADO IS 'Estado del plan: ACTIVO o INACTIVO.';


CREATE TABLE USUARIOS (
    ID_USUARIO          NUMBER(10) GENERATED BY DEFAULT ON NULL AS IDENTITY,
    ID_CIUDAD           NUMBER(10) NOT NULL,
    NOMBRE              VARCHAR2(120) NOT NULL,
    EMAIL               VARCHAR2(150) NOT NULL,
    TELEFONO            VARCHAR2(30),
    FECHA_NACIMIENTO    DATE NOT NULL,
    FECHA_REGISTRO      DATE DEFAULT SYSDATE NOT NULL,
    ESTADO_CUENTA       VARCHAR2(20) DEFAULT 'ACTIVO' NOT NULL,
    FECHA_ULTIMO_PAGO   DATE,

    CONSTRAINT PK_USUARIOS PRIMARY KEY (ID_USUARIO),
    CONSTRAINT UQ_USUARIOS_EMAIL UNIQUE (EMAIL),
    CONSTRAINT CK_USUARIOS_ESTADO CHECK (
        ESTADO_CUENTA IN ('ACTIVO', 'INACTIVO', 'SUSPENDIDO', 'ELIMINADO')
    ),
    CONSTRAINT FK_USUARIOS_CIUDAD FOREIGN KEY (ID_CIUDAD)
        REFERENCES CIUDADES(ID_CIUDAD)
);

COMMENT ON TABLE USUARIOS IS 'Usuarios principales registrados en la plataforma QuindioFlix.';
COMMENT ON COLUMN USUARIOS.ID_USUARIO IS 'Identificador unico del usuario.';
COMMENT ON COLUMN USUARIOS.ID_CIUDAD IS 'Ciudad de residencia del usuario.';
COMMENT ON COLUMN USUARIOS.NOMBRE IS 'Nombre completo del usuario.';
COMMENT ON COLUMN USUARIOS.EMAIL IS 'Correo electronico unico del usuario.';
COMMENT ON COLUMN USUARIOS.TELEFONO IS 'Telefono de contacto del usuario.';
COMMENT ON COLUMN USUARIOS.FECHA_NACIMIENTO IS 'Fecha de nacimiento del usuario.';
COMMENT ON COLUMN USUARIOS.FECHA_REGISTRO IS 'Fecha en la que el usuario se registro en la plataforma.';
COMMENT ON COLUMN USUARIOS.ESTADO_CUENTA IS 'Estado de la cuenta: ACTIVO, INACTIVO, SUSPENDIDO o ELIMINADO.';
COMMENT ON COLUMN USUARIOS.FECHA_ULTIMO_PAGO IS 'Fecha del ultimo pago exitoso realizado por el usuario.';


/* ============================================================
   2. SUSCRIPCIONES, CAMBIOS DE PLAN, PERFILES Y ROLES
   ============================================================ */

CREATE TABLE SUSCRIPCIONES (
    ID_SUSCRIPCION       NUMBER(10) GENERATED BY DEFAULT ON NULL AS IDENTITY,
    ID_USUARIO           NUMBER(10) NOT NULL,
    ID_PLAN              NUMBER(10) NOT NULL,
    FECHA_INICIO         DATE NOT NULL,
    FECHA_FIN            DATE,
    ESTADO_SUSCRIPCION   VARCHAR2(20) DEFAULT 'ACTIVA' NOT NULL,

    CONSTRAINT PK_SUSCRIPCIONES PRIMARY KEY (ID_SUSCRIPCION),
    CONSTRAINT CK_SUSC_ESTADO CHECK (
        ESTADO_SUSCRIPCION IN ('ACTIVA', 'FINALIZADA', 'CANCELADA', 'SUSPENDIDA')
    ),
    CONSTRAINT CK_SUSC_FECHAS CHECK (
        FECHA_FIN IS NULL OR FECHA_FIN >= FECHA_INICIO
    ),
    CONSTRAINT FK_SUSC_USUARIO FOREIGN KEY (ID_USUARIO)
        REFERENCES USUARIOS(ID_USUARIO),
    CONSTRAINT FK_SUSC_PLAN FOREIGN KEY (ID_PLAN)
        REFERENCES PLANES_SUSCRIPCION(ID_PLAN)
);

COMMENT ON TABLE SUSCRIPCIONES IS 'Historial de planes de suscripcion asociados a cada usuario.';
COMMENT ON COLUMN SUSCRIPCIONES.ID_SUSCRIPCION IS 'Identificador unico de la suscripcion.';
COMMENT ON COLUMN SUSCRIPCIONES.ID_USUARIO IS 'Usuario propietario de la suscripcion.';
COMMENT ON COLUMN SUSCRIPCIONES.ID_PLAN IS 'Plan asociado a la suscripcion.';
COMMENT ON COLUMN SUSCRIPCIONES.FECHA_INICIO IS 'Fecha de inicio de la suscripcion.';
COMMENT ON COLUMN SUSCRIPCIONES.FECHA_FIN IS 'Fecha de finalizacion de la suscripcion.';
COMMENT ON COLUMN SUSCRIPCIONES.ESTADO_SUSCRIPCION IS 'Estado de la suscripcion.';


CREATE UNIQUE INDEX UQ_SUSC_ACTIVA_USU
ON SUSCRIPCIONES (
    CASE WHEN ESTADO_SUSCRIPCION = 'ACTIVA' THEN ID_USUARIO END
);


CREATE TABLE CAMBIOS_PLAN (
    ID_CAMBIO_PLAN    NUMBER(10) GENERATED BY DEFAULT ON NULL AS IDENTITY,
    ID_USUARIO        NUMBER(10) NOT NULL,
    ID_PLAN_ANTERIOR  NUMBER(10) NOT NULL,
    ID_PLAN_NUEVO     NUMBER(10) NOT NULL,
    FECHA_CAMBIO      TIMESTAMP DEFAULT SYSTIMESTAMP NOT NULL,
    MOTIVO            VARCHAR2(300),

    CONSTRAINT PK_CAMBIOS_PLAN PRIMARY KEY (ID_CAMBIO_PLAN),
    CONSTRAINT CK_CAMBIOS_PLAN_DIF CHECK (ID_PLAN_ANTERIOR <> ID_PLAN_NUEVO),
    CONSTRAINT FK_CAMBIOS_USUARIO FOREIGN KEY (ID_USUARIO)
        REFERENCES USUARIOS(ID_USUARIO),
    CONSTRAINT FK_CAMBIOS_PLAN_ANT FOREIGN KEY (ID_PLAN_ANTERIOR)
        REFERENCES PLANES_SUSCRIPCION(ID_PLAN),
    CONSTRAINT FK_CAMBIOS_PLAN_NUEVO FOREIGN KEY (ID_PLAN_NUEVO)
        REFERENCES PLANES_SUSCRIPCION(ID_PLAN)
);

COMMENT ON TABLE CAMBIOS_PLAN IS 'Auditoria de cambios de plan realizados por los usuarios.';
COMMENT ON COLUMN CAMBIOS_PLAN.ID_CAMBIO_PLAN IS 'Identificador unico del cambio de plan.';
COMMENT ON COLUMN CAMBIOS_PLAN.ID_USUARIO IS 'Usuario que realiza el cambio de plan.';
COMMENT ON COLUMN CAMBIOS_PLAN.ID_PLAN_ANTERIOR IS 'Plan que tenia el usuario antes del cambio.';
COMMENT ON COLUMN CAMBIOS_PLAN.ID_PLAN_NUEVO IS 'Nuevo plan seleccionado por el usuario.';
COMMENT ON COLUMN CAMBIOS_PLAN.FECHA_CAMBIO IS 'Fecha y hora del cambio de plan.';
COMMENT ON COLUMN CAMBIOS_PLAN.MOTIVO IS 'Motivo o descripcion del cambio de plan.';


CREATE TABLE PERFILES (
    ID_PERFIL       NUMBER(10) GENERATED BY DEFAULT ON NULL AS IDENTITY,
    ID_USUARIO      NUMBER(10) NOT NULL,
    NOMBRE_PERFIL   VARCHAR2(80) NOT NULL,
    AVATAR          VARCHAR2(250),
    TIPO_PERFIL     VARCHAR2(20) NOT NULL,
    FECHA_CREACION  DATE DEFAULT SYSDATE NOT NULL,
    ESTADO          VARCHAR2(20) DEFAULT 'ACTIVO' NOT NULL,

    CONSTRAINT PK_PERFILES PRIMARY KEY (ID_PERFIL),
    CONSTRAINT UQ_PERFILES_USU_NOM UNIQUE (ID_USUARIO, NOMBRE_PERFIL),
    CONSTRAINT CK_PERFILES_TIPO CHECK (TIPO_PERFIL IN ('ADULTO', 'INFANTIL')),
    CONSTRAINT CK_PERFILES_ESTADO CHECK (ESTADO IN ('ACTIVO', 'INACTIVO')),
    CONSTRAINT FK_PERFILES_USUARIO FOREIGN KEY (ID_USUARIO)
        REFERENCES USUARIOS(ID_USUARIO)
);

COMMENT ON TABLE PERFILES IS 'Perfiles de visualizacion asociados a una cuenta de usuario.';
COMMENT ON COLUMN PERFILES.ID_PERFIL IS 'Identificador unico del perfil.';
COMMENT ON COLUMN PERFILES.ID_USUARIO IS 'Usuario propietario del perfil.';
COMMENT ON COLUMN PERFILES.NOMBRE_PERFIL IS 'Nombre visible del perfil.';
COMMENT ON COLUMN PERFILES.AVATAR IS 'Ruta o identificador del avatar del perfil.';
COMMENT ON COLUMN PERFILES.TIPO_PERFIL IS 'Tipo de perfil: ADULTO o INFANTIL.';
COMMENT ON COLUMN PERFILES.FECHA_CREACION IS 'Fecha de creacion del perfil.';
COMMENT ON COLUMN PERFILES.ESTADO IS 'Estado del perfil: ACTIVO o INACTIVO.';


CREATE TABLE ROLES_USUARIO (
    ID_ROL_USUARIO  NUMBER(10) GENERATED BY DEFAULT ON NULL AS IDENTITY,
    NOMBRE_ROL      VARCHAR2(50) NOT NULL,
    DESCRIPCION     VARCHAR2(300),

    CONSTRAINT PK_ROLES_USUARIO PRIMARY KEY (ID_ROL_USUARIO),
    CONSTRAINT UQ_ROLES_USUARIO_NOMBRE UNIQUE (NOMBRE_ROL)
);

COMMENT ON TABLE ROLES_USUARIO IS 'Roles funcionales que puede tener un usuario dentro de la plataforma.';
COMMENT ON COLUMN ROLES_USUARIO.ID_ROL_USUARIO IS 'Identificador unico del rol.';
COMMENT ON COLUMN ROLES_USUARIO.NOMBRE_ROL IS 'Nombre del rol: CLIENTE, MODERADOR o ADMIN.';
COMMENT ON COLUMN ROLES_USUARIO.DESCRIPCION IS 'Descripcion funcional del rol.';


CREATE TABLE USUARIOS_ROLES (
    ID_USUARIO        NUMBER(10) NOT NULL,
    ID_ROL_USUARIO    NUMBER(10) NOT NULL,
    FECHA_ASIGNACION  DATE DEFAULT SYSDATE NOT NULL,
    ESTADO            VARCHAR2(20) DEFAULT 'ACTIVO' NOT NULL,

    CONSTRAINT PK_USUARIOS_ROLES PRIMARY KEY (ID_USUARIO, ID_ROL_USUARIO),
    CONSTRAINT CK_USUARIOS_ROLES_EST CHECK (ESTADO IN ('ACTIVO', 'INACTIVO')),
    CONSTRAINT FK_USU_ROLES_USUARIO FOREIGN KEY (ID_USUARIO)
        REFERENCES USUARIOS(ID_USUARIO) ON DELETE CASCADE,
    CONSTRAINT FK_USU_ROLES_ROL FOREIGN KEY (ID_ROL_USUARIO)
        REFERENCES ROLES_USUARIO(ID_ROL_USUARIO)
);

COMMENT ON TABLE USUARIOS_ROLES IS 'Tabla intermedia entre usuarios y roles funcionales.';
COMMENT ON COLUMN USUARIOS_ROLES.ID_USUARIO IS 'Usuario al que se asigna el rol.';
COMMENT ON COLUMN USUARIOS_ROLES.ID_ROL_USUARIO IS 'Rol asignado al usuario.';
COMMENT ON COLUMN USUARIOS_ROLES.FECHA_ASIGNACION IS 'Fecha en la que se asigno el rol.';
COMMENT ON COLUMN USUARIOS_ROLES.ESTADO IS 'Estado de la asignacion del rol.';


/* ============================================================
   3. PAGOS, REFERIDOS Y BENEFICIOS
   ============================================================ */

CREATE TABLE PAGOS (
    ID_PAGO             NUMBER(10) GENERATED BY DEFAULT ON NULL AS IDENTITY,
    ID_USUARIO          NUMBER(10) NOT NULL,
    ID_PLAN             NUMBER(10) NOT NULL,
    FECHA_PAGO          TIMESTAMP DEFAULT SYSTIMESTAMP NOT NULL,
    FECHA_VENCIMIENTO   DATE NOT NULL,
    MONTO_BRUTO         NUMBER(10,2) NOT NULL,
    DESCUENTO_APLICADO  NUMBER(10,2) DEFAULT 0 NOT NULL,
    MONTO_PAGADO        NUMBER(10,2) NOT NULL,
    METODO_PAGO         VARCHAR2(30) NOT NULL,
    ESTADO_PAGO         VARCHAR2(20) NOT NULL,
    REFERENCIA_PAGO     VARCHAR2(100),

    CONSTRAINT PK_PAGOS PRIMARY KEY (ID_PAGO),
    CONSTRAINT UQ_PAGOS_REFERENCIA UNIQUE (REFERENCIA_PAGO),
    CONSTRAINT CK_PAGOS_MONTO_BRUTO CHECK (MONTO_BRUTO > 0),
    CONSTRAINT CK_PAGOS_DESCUENTO CHECK (DESCUENTO_APLICADO >= 0),
    CONSTRAINT CK_PAGOS_MONTO_PAGADO CHECK (MONTO_PAGADO >= 0),
    CONSTRAINT CK_PAGOS_DESC_LIMITE CHECK (DESCUENTO_APLICADO <= MONTO_BRUTO),
    CONSTRAINT CK_PAGOS_METODO CHECK (
        METODO_PAGO IN ('TARJETA_CREDITO', 'TARJETA_DEBITO', 'PSE', 'NEQUI', 'DAVIPLATA')
    ),
    CONSTRAINT CK_PAGOS_ESTADO CHECK (
        ESTADO_PAGO IN ('EXITOSO', 'FALLIDO', 'PENDIENTE', 'REEMBOLSADO')
    ),
    CONSTRAINT FK_PAGOS_USUARIO FOREIGN KEY (ID_USUARIO)
        REFERENCES USUARIOS(ID_USUARIO),
    CONSTRAINT FK_PAGOS_PLAN FOREIGN KEY (ID_PLAN)
        REFERENCES PLANES_SUSCRIPCION(ID_PLAN)
);

COMMENT ON TABLE PAGOS IS 'Pagos mensuales realizados por los usuarios por concepto de suscripcion.';
COMMENT ON COLUMN PAGOS.ID_PAGO IS 'Identificador unico del pago.';
COMMENT ON COLUMN PAGOS.ID_USUARIO IS 'Usuario que realiza el pago.';
COMMENT ON COLUMN PAGOS.ID_PLAN IS 'Plan cobrado en el pago.';
COMMENT ON COLUMN PAGOS.FECHA_PAGO IS 'Fecha y hora en la que se registra el pago.';
COMMENT ON COLUMN PAGOS.FECHA_VENCIMIENTO IS 'Fecha de vencimiento del periodo de pago.';
COMMENT ON COLUMN PAGOS.MONTO_BRUTO IS 'Valor original del cobro antes de descuentos.';
COMMENT ON COLUMN PAGOS.DESCUENTO_APLICADO IS 'Descuento aplicado al pago.';
COMMENT ON COLUMN PAGOS.MONTO_PAGADO IS 'Valor final pagado por el usuario.';
COMMENT ON COLUMN PAGOS.METODO_PAGO IS 'Metodo de pago usado por el usuario.';
COMMENT ON COLUMN PAGOS.ESTADO_PAGO IS 'Estado del pago: EXITOSO, FALLIDO, PENDIENTE o REEMBOLSADO.';
COMMENT ON COLUMN PAGOS.REFERENCIA_PAGO IS 'Referencia externa o codigo de transaccion del pago.';


CREATE TABLE REFERIDOS (
    ID_REFERIDO             NUMBER(10) GENERATED BY DEFAULT ON NULL AS IDENTITY,
    ID_USUARIO_REFERIDOR    NUMBER(10) NOT NULL,
    ID_USUARIO_REFERIDO     NUMBER(10) NOT NULL,
    FECHA_REFERIDO          DATE DEFAULT SYSDATE NOT NULL,
    ESTADO_REFERIDO         VARCHAR2(20) DEFAULT 'ACTIVO' NOT NULL,

    CONSTRAINT PK_REFERIDOS PRIMARY KEY (ID_REFERIDO),
    CONSTRAINT UQ_REFERIDOS_REFERIDO UNIQUE (ID_USUARIO_REFERIDO),
    CONSTRAINT CK_REFERIDOS_DIF CHECK (ID_USUARIO_REFERIDOR <> ID_USUARIO_REFERIDO),
    CONSTRAINT CK_REFERIDOS_ESTADO CHECK (
        ESTADO_REFERIDO IN ('ACTIVO', 'APLICADO', 'CANCELADO')
    ),
    CONSTRAINT FK_REF_USU_REFERIDOR FOREIGN KEY (ID_USUARIO_REFERIDOR)
        REFERENCES USUARIOS(ID_USUARIO),
    CONSTRAINT FK_REF_USU_REFERIDO FOREIGN KEY (ID_USUARIO_REFERIDO)
        REFERENCES USUARIOS(ID_USUARIO)
);

COMMENT ON TABLE REFERIDOS IS 'Relacion recursiva que registra que usuario refirio a otro usuario.';
COMMENT ON COLUMN REFERIDOS.ID_REFERIDO IS 'Identificador unico del referido.';
COMMENT ON COLUMN REFERIDOS.ID_USUARIO_REFERIDOR IS 'Usuario que realiza la referencia.';
COMMENT ON COLUMN REFERIDOS.ID_USUARIO_REFERIDO IS 'Usuario nuevo que fue referido.';
COMMENT ON COLUMN REFERIDOS.FECHA_REFERIDO IS 'Fecha en la que se registra el referido.';
COMMENT ON COLUMN REFERIDOS.ESTADO_REFERIDO IS 'Estado del referido.';


CREATE TABLE BENEFICIOS_REFERIDO (
    ID_BENEFICIO             NUMBER(10) GENERATED BY DEFAULT ON NULL AS IDENTITY,
    ID_REFERIDO              NUMBER(10) NOT NULL,
    ID_USUARIO_BENEFICIARIO  NUMBER(10) NOT NULL,
    TIPO_BENEFICIO           VARCHAR2(40) NOT NULL,
    VALOR_DESCUENTO          NUMBER(10,2) DEFAULT 0 NOT NULL,
    ESTADO_BENEFICIO         VARCHAR2(20) DEFAULT 'PENDIENTE' NOT NULL,
    FECHA_GENERACION         DATE DEFAULT SYSDATE NOT NULL,
    FECHA_APLICACION         DATE,
    ID_PAGO_APLICADO         NUMBER(10),

    CONSTRAINT PK_BENEFICIOS_REFERIDO PRIMARY KEY (ID_BENEFICIO),
    CONSTRAINT UQ_BEN_REF_USU UNIQUE (ID_REFERIDO, ID_USUARIO_BENEFICIARIO),
    CONSTRAINT CK_BEN_TIPO CHECK (
        TIPO_BENEFICIO IN ('DESCUENTO', 'MES_GRATIS', 'BONO')
    ),
    CONSTRAINT CK_BEN_VALOR CHECK (VALOR_DESCUENTO >= 0),
    CONSTRAINT CK_BEN_ESTADO CHECK (
        ESTADO_BENEFICIO IN ('PENDIENTE', 'APLICADO', 'VENCIDO', 'CANCELADO')
    ),
    CONSTRAINT FK_BEN_REFERIDO FOREIGN KEY (ID_REFERIDO)
        REFERENCES REFERIDOS(ID_REFERIDO),
    CONSTRAINT FK_BEN_USUARIO FOREIGN KEY (ID_USUARIO_BENEFICIARIO)
        REFERENCES USUARIOS(ID_USUARIO),
    CONSTRAINT FK_BEN_PAGO FOREIGN KEY (ID_PAGO_APLICADO)
        REFERENCES PAGOS(ID_PAGO)
);

COMMENT ON TABLE BENEFICIOS_REFERIDO IS 'Beneficios generados por el sistema de referidos.';
COMMENT ON COLUMN BENEFICIOS_REFERIDO.ID_BENEFICIO IS 'Identificador unico del beneficio.';
COMMENT ON COLUMN BENEFICIOS_REFERIDO.ID_REFERIDO IS 'Referido que origina el beneficio.';
COMMENT ON COLUMN BENEFICIOS_REFERIDO.ID_USUARIO_BENEFICIARIO IS 'Usuario que recibe el beneficio.';
COMMENT ON COLUMN BENEFICIOS_REFERIDO.TIPO_BENEFICIO IS 'Tipo de beneficio otorgado.';
COMMENT ON COLUMN BENEFICIOS_REFERIDO.VALOR_DESCUENTO IS 'Valor monetario del descuento.';
COMMENT ON COLUMN BENEFICIOS_REFERIDO.ESTADO_BENEFICIO IS 'Estado del beneficio.';
COMMENT ON COLUMN BENEFICIOS_REFERIDO.FECHA_GENERACION IS 'Fecha en que se genera el beneficio.';
COMMENT ON COLUMN BENEFICIOS_REFERIDO.FECHA_APLICACION IS 'Fecha en que se aplica el beneficio.';
COMMENT ON COLUMN BENEFICIOS_REFERIDO.ID_PAGO_APLICADO IS 'Pago sobre el cual se aplico el beneficio.';


