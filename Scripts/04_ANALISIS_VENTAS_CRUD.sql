--VISTA VW_USUARIOS_SEGURA

CREATE OR REPLACE VIEW VW_USUARIOS_SEGURA AS
SELECT
    U.ID_USUARIO,
    U.NOMBRE,
    SUBSTR(U.EMAIL, 1, 2) || '***' || SUBSTR(U.EMAIL, INSTR(U.EMAIL, '@')) AS EMAIL_OCULTO,
    SUBSTR(U.TELEFONO, 1, 3) || '****' || SUBSTR(U.TELEFONO, -2) AS TELEFONO_OCULTO,
    C.NOMBRE_CIUDAD,
    C.DEPARTAMENTO,
    U.FECHA_REGISTRO,
    U.ESTADO_CUENTA,
    PL.NOMBRE_PLAN,
    COUNT(P.ID_PERFIL) AS TOTAL_PERFILES
FROM USUARIOS U
JOIN CIUDADES C
    ON C.ID_CIUDAD = U.ID_CIUDAD
LEFT JOIN SUSCRIPCIONES S
    ON S.ID_USUARIO = U.ID_USUARIO
   AND S.ESTADO_SUSCRIPCION = 'ACTIVA'
LEFT JOIN PLANES_SUSCRIPCION PL
    ON PL.ID_PLAN = S.ID_PLAN
LEFT JOIN PERFILES P
    ON P.ID_USUARIO = U.ID_USUARIO
   AND P.ESTADO = 'ACTIVO'
GROUP BY
    U.ID_USUARIO,
    U.NOMBRE,
    U.EMAIL,
    U.TELEFONO,
    C.NOMBRE_CIUDAD,
    C.DEPARTAMENTO,
    U.FECHA_REGISTRO,
    U.ESTADO_CUENTA,
    PL.NOMBRE_PLAN;
    
    --VW_CATALOGO_PUBLICO
    CREATE OR REPLACE VIEW VW_CATALOGO_PUBLICO AS
SELECT
    C.ID_CONTENIDO,
    C.TITULO,
    CAT.NOMBRE_CATEGORIA,
    LISTAGG(G.NOMBRE_GENERO, ', ')
        WITHIN GROUP (ORDER BY G.NOMBRE_GENERO) AS GENEROS,
    C.ANIO_LANZAMIENTO,
    C.DURACION_MINUTOS,
    C.CLASIFICACION_EDAD,
    C.ES_ORIGINAL,
    C.POPULARIDAD
FROM CONTENIDOS C
JOIN CATEGORIAS_CONTENIDO CAT
    ON CAT.ID_CATEGORIA = C.ID_CATEGORIA
LEFT JOIN CONTENIDOS_GENEROS CG
    ON CG.ID_CONTENIDO = C.ID_CONTENIDO
LEFT JOIN GENEROS G
    ON G.ID_GENERO = CG.ID_GENERO
WHERE C.ESTADO_CONTENIDO = 'DISPONIBLE'
GROUP BY
    C.ID_CONTENIDO,
    C.TITULO,
    CAT.NOMBRE_CATEGORIA,
    C.ANIO_LANZAMIENTO,
    C.DURACION_MINUTOS,
    C.CLASIFICACION_EDAD,
    C.ES_ORIGINAL,
    C.POPULARIDAD;
    
    --VW_GESTION_CATALOGO_CRUD
    CREATE OR REPLACE VIEW VW_GESTION_CATALOGO_CRUD AS
SELECT
    ID_CONTENIDO,
    ID_CATEGORIA,
    ID_EMPLEADO_RESPONSABLE,
    TITULO,
    ANIO_LANZAMIENTO,
    DURACION_MINUTOS,
    SINOPSIS,
    CLASIFICACION_EDAD,
    FECHA_AGREGADO_CATALOGO,
    ES_ORIGINAL,
    ESTADO_CONTENIDO,
    POPULARIDAD
FROM CONTENIDOS
WHERE ESTADO_CONTENIDO IN ('DISPONIBLE', 'OCULTO')
WITH CHECK OPTION CONSTRAINT CK_VW_GESTION_CATALOGO;

