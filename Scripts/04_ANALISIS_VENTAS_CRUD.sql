--VW_REPORTE_INGRESOS_MENSUALES
CREATE OR REPLACE VIEW VW_REPORTE_INGRESOS_MENSUALES AS
SELECT
    EXTRACT(YEAR FROM P.FECHA_PAGO) AS ANIO,
    EXTRACT(MONTH FROM P.FECHA_PAGO) AS MES,
    C.NOMBRE_CIUDAD,
    PL.NOMBRE_PLAN,
    COUNT(P.ID_PAGO) AS TOTAL_PAGOS,
    SUM(P.MONTO_BRUTO) AS MONTO_BRUTO_TOTAL,
    SUM(P.DESCUENTO_APLICADO) AS DESCUENTOS_TOTALES,
    SUM(P.MONTO_PAGADO) AS INGRESOS_NETOS
FROM PAGOS P
JOIN USUARIOS U
    ON U.ID_USUARIO = P.ID_USUARIO
JOIN CIUDADES C
    ON C.ID_CIUDAD = U.ID_CIUDAD
JOIN PLANES_SUSCRIPCION PL
    ON PL.ID_PLAN = P.ID_PLAN
WHERE P.ESTADO_PAGO = 'EXITOSO'
GROUP BY
    EXTRACT(YEAR FROM P.FECHA_PAGO),
    EXTRACT(MONTH FROM P.FECHA_PAGO),
    C.NOMBRE_CIUDAD,
    PL.NOMBRE_PLAN;
    
    --VW_REPORTE_CONSUMO_CONTENIDO
    CREATE OR REPLACE VIEW VW_REPORTE_CONSUMO_CONTENIDO AS
SELECT
    CAT.NOMBRE_CATEGORIA,
    C.ID_CONTENIDO,
    C.TITULO,
    R.DISPOSITIVO,
    COUNT(R.ID_REPRODUCCION) AS TOTAL_REPRODUCCIONES,
    ROUND(AVG(R.PORCENTAJE_AVANCE), 2) AS AVANCE_PROMEDIO,
    SUM(
        CASE
            WHEN R.PORCENTAJE_AVANCE >= 90 THEN 1
            ELSE 0
        END
    ) AS REPRODUCCIONES_COMPLETAS
FROM REPRODUCCIONES R
JOIN CONTENIDOS C
    ON C.ID_CONTENIDO = R.ID_CONTENIDO
JOIN CATEGORIAS_CONTENIDO CAT
    ON CAT.ID_CATEGORIA = C.ID_CATEGORIA
GROUP BY
    CAT.NOMBRE_CATEGORIA,
    C.ID_CONTENIDO,
    C.TITULO,
    R.DISPOSITIVO;
    
    
